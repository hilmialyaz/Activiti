package org.milleni.dunning.ui.prcstart;

public class TaskDefObject {
	private String name;
	private String desc;
	
	public TaskDefObject(String name, String desc) {
		super();
		this.name = name;
		this.desc = desc;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
}